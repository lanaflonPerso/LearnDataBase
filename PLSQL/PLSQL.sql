DECLARE
  v_emp_name employees.last_name%TYPE; 
  v_emp_salary employees.salary%TYPE;
BEGIN
  SELECT last_name,
         salary
  INTO v_emp_name,
       v_emp_salary
  FROM employees
  WHERE employee_id = 100;
  DBMS_OUTPUT.PUT_LINE('My name is: '|| v_emp_name );
  DBMS_OUTPUT.PUT_LINE('Salary is: '|| v_emp_salary );
END;
/


